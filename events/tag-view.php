        function expan(node, e) {
            if (node.children("ul").is(":hidden")) {
                if (e.type == "click")
                    node.children("ul").slideDown("slow")//removeClass("collapse");
            }
            else {
                if (e.type == "click")
                    node.children("ul").slideUp("slow");
            }
            e.stopPropagation();
        }
